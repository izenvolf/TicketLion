package team.location.db;

public class LocationListBean {
	
	private int l_num;
	private String p_code;
	private String l_img;
	private String l_name;
	private String l_address;
	
	
	public String getL_address() {
		return l_address;
	}
	public void setL_address(String l_address) {
		this.l_address = l_address;
	}
	public int getL_num() {
		return l_num;
	}
	public void setL_num(int l_num) {
		this.l_num = l_num;
	}
	public String getP_code() {
		return p_code;
	}
	public void setP_code(String p_code) {
		this.p_code = p_code;
	}
	public String getL_img() {
		return l_img;
	}
	public void setL_img(String l_img) {
		this.l_img = l_img;
	}
	public String getL_name() {
		return l_name;
	}
	public void setL_name(String l_name) {
		this.l_name = l_name;
	}
	
	
	
	
}
